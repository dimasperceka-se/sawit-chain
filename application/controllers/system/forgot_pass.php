<script>
    function error(e) {
        e.add('error');
    }

    function rmError (e) {
        e.remove('error');
    }

    function validationEmail() {
        let form = document.getElementById('form-input')
        let email = document.getElementById('username').value
        let text = document.getElementById('valEmail')
        let pattern = /^[^ ]+@[^ ]+\.[a-z]{2,3}$/

        if (email.match(pattern)) {
            form.classList.add('valid')
            form.classList.remove('invalid')
            rmError(form.classList)
            text.innerHTML = ""
            text.style.color = ''
        } else {
            form.classList.remove('valid')
            form.classList.add('invalid')
            error(form.classList)
            text.innerHTML = "Invalid email address."
            text.style.color = '#d02630'
            text.style.fontSize = '14px'
            text.style.marginTop = '8px'
        }

        if (email == '') {
            form.classList.remove('valid')
            form.classList.add('invalid')
            console.log("masak")
            error(form.classList)
            text.innerHTML = "Invalid email address."
            text.style.color = '#d02630'
        }
    }
    $( document ).ready(function() {
        $(document).on('click', '#btnSubmit', function(e) {
            e.preventDefault();
            $("#ajaxImgLoad").show();

            $("#boxFailed").html('');
            $("#boxFailed").hide();
            $("#boxSuccess").html('');
            $("#boxSuccess").hide();

            let validationStatus = true;
            let validationMsg = [];
            let validationMsgDisplay;

            if(document.getElementById("username").value == "") {
                validationStatus = false;
                validationMsg.push('Username is required');
            }
            
            if(validationStatus == false) {
                validationMsgDisplay = '<img src="/assets/new/img/alert-circle.svg" alt="Alert" /><p>';
                for (var index = 0; index < validationMsg.length; index++) {
                    validationMsgDisplay += '<p>'+validationMsg[index]+'</p>'
                }
                validationMsgDisplay += '</p>';

                $('#boxFailed').html(validationMsgDisplay);
                $("#boxFailed").css("display", "flex");
                $("#ajaxImgLoad").hide();
            } else {
                $.ajax({
                    type: "POST",
                    url: '<?php echo $api_url?>'+'/page/front_req_forgot_password',
                    data: {
                        username: document.getElementById("username").value
                    },
                    success: function(r){
                        $("#ajaxImgLoad").hide();
                        // direct update password
                        // window.location = r.page;
                        window.location = "/system/login/forgot_verification_code";
                    },
                    error: function(r){
                        $("#ajaxImgLoad").hide();
                        validationMsgDisplay = '<img src="/assets/new/img/alert-circle.svg" alt="Alert" /><p>';
                        try {
                            let objR = JSON.parse(r.responseText);
                            validationMsgDisplay += objR.message + '</p>';
                            $('#boxFailed').html(validationMsgDisplay);
                            $("#boxFailed").css("display", "flex");
                        }
                        catch(err) {
                            validationMsgDisplay += 'Process failed, Please try again later </p>';
                            $('#boxFailed').html(validationMsgDisplay);
                            $("#boxFailed").css("display", "flex");
                        }
                    }
                });
            }
        });
    });
</script>